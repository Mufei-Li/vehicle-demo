// ÂàùÂßãÂåñÊï∞ÊçÆÂ∫ì
function openDB() {
  return new Promise((resolve, reject) => {
    const request = indexedDB.open("VehicleAppDB", 1);
    request.onupgradeneeded = (event) => {
      const db = event.target.result;
      if (!db.objectStoreNames.contains("videos")) {
        db.createObjectStore("videos", { keyPath: "id" });
      }
    };
    request.onsuccess = () => resolve(request.result);
    request.onerror = (e) => reject(e);
  });
}

// ‰øùÂ≠òÊñá‰ª∂ÔºàÂèØÈù†ÁöÑ Promise ÂÆûÁé∞Ôºâ
async function saveVideoToDB(id, file) {
  const db = await openDB();
  return new Promise((resolve, reject) => {
    try {
      const tx = db.transaction("videos", "readwrite");
      const store = tx.objectStore("videos");
      const req = store.put({ id, file });
      req.onsuccess = () => resolve(true);
      req.onerror = (e) => reject(e);
      // ‰øùÈô©ÔºöÂú®‰∫ãÂä°ÂÆåÊàêÊó∂‰πü resolveÔºàÈò≤Ê≠¢Êüê‰∫õÊµèËßàÂô®‰∏çËß¶Âèë req.onsuccessÔºâ
      tx.oncomplete = () => resolve(true);
      tx.onerror = (e) => reject(e);
      tx.onabort = (e) => reject(e);
    } catch (err) {
      reject(err);
    }
  });
}

